let mapWidth = worldMap[0].length;
let mapHeight = worldMap.length;
var cnv;
function setup(){
    cnv = createCanvas(windowWidth,windowHeight);
}
function draw(){
    drawTileSet(mapWidth,mapHeight);
}


function drawTileSet(width,height){
    background(0);
    for (var y = 0; y < height; y++) {
        for (var x = 0; x < width; x++) {
            var wall = worldMap[y][x];
            if (wall > 0) {
                push();
                drawSingleTile(x,y,red,15);
                pop();
            }
            else {
                push();
                drawSingleTile(x,y,blue,15);
                pop();
            }
        }
    }
}

function drawSingleTile(x,y,color,scale){
    if(color == blue){
        fill(0,0,255);
    }
    if(color == red){
        fill(255);
    }
    rect(x * scale, y * scale, scale, scale);
    if(mouseX>x*scale && mouseX<(x+1)*scale && mouseY>y*scale && mouseY<(y+1)*scale){
        push();
        fill(255,0,0);
        rect(x*scale, y *scale, scale, scale);
        worldMap[y][x] = 0;
        pop();
    }
}


